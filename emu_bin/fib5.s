# fib5.s - 计算斐波那契第5项 (result = 5)
# 纯指令实现，无数据区，硬编码常数
# 斐波那契序列: 0, 1, 1, 2, 3, 5...

.section .text
.globl _start

_start:
    # 斐波那契迭代计算，第5项 (从第0项开始计数)
    # f(0) = 0, f(1) = 1
    # 使用 a0, a1 存储相邻两项，t0 作为临时
    
    li   t1, 0          # f(0) = 0  (第0项)
    li   t2, 1          # f(1) = 1  (第1项)
    
    # 第2项: f(2) = f(0) + f(1) = 1
    add  t3, t1, t2     # t3 = 0 + 1 = 1
    mv   t1, t2         # 滑动窗口: t1 = 1 (旧f(1))
    mv   t2, t3         # 滑动窗口: t2 = 1 (新f(2))
    
    # 第3项: f(3) = f(1) + f(2) = 2
    add  t3, t1, t2     # t3 = 1 + 1 = 2
    mv   t1, t2         # t1 = 1
    mv   t2, t3         # t2 = 2
    
    # 第4项: f(4) = f(2) + f(3) = 3
    add  t3, t1, t2     # t3 = 1 + 2 = 3
    mv   t1, t2         # t1 = 2
    mv   t2, t3         # t2 = 3
    
    # 第5项: f(5) = f(3) + f(4) = 5
    add  a0, t1, t2     # a0 = 2 + 3 = 5 (最终结果)
    
    # 程序结束 (无限循环或退出)
    # 在裸机/模拟器中通常使用 ebreak 或自旋
    ebreak              # 触发断点，方便调试观察结果

# 结果: a0 = 5
